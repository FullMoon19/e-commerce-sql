                        AddessID INT AUTO_INCREMENT PRIMARY KEY,
                        Street VARCHAR(30) NOT NULL,
                        City VARCHAR(30) NOT NULL,
                        Country VARCHAR(50) NOT NULL,
                        PostCode VARCHAR(0) NOT NULL)";
                        //UserId clés étranger ?
                $dbco->exec($sql);
